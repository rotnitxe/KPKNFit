import { ExerciseMuscleInfo } from '../types';

function mk(
  id: string,
  name: string,
  desc: string,
  equipment: ExerciseMuscleInfo['equipment'],
  type: ExerciseMuscleInfo['type'],
  category: ExerciseMuscleInfo['category'],
  force: ExerciseMuscleInfo['force'],
  efc: number,
  cnc: number,
  ssc: number,
  ttc: number,
  primary: string[],
  secondary: string[],
  stabilizer: string[],
  subMuscleGroup?: string,
  bodyPart: 'upper' | 'lower' | 'full' = 'lower'
): ExerciseMuscleInfo {
  const involvedMuscles: ExerciseMuscleInfo['involvedMuscles'] = [
    ...primary.map((m) => ({ muscle: m, role: 'primary' as const, activation: 1.0 })),
    ...secondary.map((m) => ({ muscle: m, role: 'secondary' as const, activation: 0.5 })),
    ...stabilizer.filter((m) => m !== 'Ninguno').map((m) => ({ muscle: m, role: 'stabilizer' as const, activation: 0.4 })),
  ];
  const chain: 'anterior' | 'posterior' | 'full' =
    bodyPart === 'upper'
      ? force === 'Tirón' ? 'posterior' : force === 'Empuje' ? 'anterior' : 'full'
      : force === 'Bisagra' ? 'posterior' : force === 'Sentadilla' || force === 'Extensión' ? 'anterior' : 'full';
  return {
    id,
    name,
    description: desc,
    involvedMuscles,
    subMuscleGroup: subMuscleGroup ?? primary[0],
    category,
    type,
    equipment,
    force,
    bodyPart,
    chain,
    efc,
    cnc,
    ssc,
    ttc,
  };
}

