export function normalizeText(value: string): string {
  return value
    .toLowerCase()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .trim();
}

export function searchFoods(foods: FoodItem[], query: string): FoodItem[] {
  const normalizedQuery = normalizeText(query);
  if (!normalizedQuery) return foods;

  return foods.filter((food) => {
    const searchableFields = [
      food.name,
      food.brand || '',
      ...(food.tags || []),
      ...(food.aliases || []),
    ];

    return searchableFields.some(field => 
      normalizeText(field).includes(normalizedQuery)
    );
  });
}

export function filterFoodsByCategory(
  foods: FoodItem[],
  category: FoodCategory | 'Todos' | 'todos'
): FoodItem[] {
  if (category === 'Todos' || category === 'todos') return foods;
  return foods.filter((food) => food.category === category);
}
